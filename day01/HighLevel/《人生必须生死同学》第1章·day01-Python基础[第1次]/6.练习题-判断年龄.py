user_input = int(input('请输入你的年龄: '))


if user_input > 90:
    print('再见了这个世界')

elif user_input > 70:
    print('人生就快结束了的一个老屁孩儿')

elif user_input > 60:
    print('活着还不错的老屁孩儿')

elif user_input > 50:
    print('自己马上变成不听话的老屁孩儿')

elif user_input > 40:
    print('家里有个不听话的小屁孩儿')

elif user_input > 30:
    print('看老大不小了, 赶紧结婚小屁孩儿')

elif user_input > 20:
    print('开始混社会的小屁孩儿')

elif user_input > 10:
    print('青春期叛逆的小屁孩')

elif user_input < 10:
    print('小屁孩')

