#!/usr/bin/env python
# -*- coding:UTF-8 -*-

while True:
	Age = int(input("请输入他的年龄:"))
	if Age <= 10:
		print("小屁孩")
	elif Age <= 20:
		print("青春期叛逆的小屁孩")
	elif Age <= 30:
		print("开始定性, 开始混社会的小屁孩儿")
	elif Age <= 40:
		print("体制看老大不小了, 赶紧结婚小屁孩儿")
	elif Age <= 50:
		print("家里有个不听话的小屁孩儿")
	elif Age <= 60:
		print("自己马上变成不听话的老屁孩儿")
	elif Age <= 70:
		print("活着还不错的老屁孩儿")
	elif Age <= 90:
		print("人生就快结束了的一个老屁孩儿")
	elif Age > 90:
		print("您长命百岁")

